public class MalformedAlignmentException extends Exception
{

	/**
	 * Serial Version UID - generated
	 */
	private static final long serialVersionUID = -3315081502612145890L;

	public MalformedAlignmentException()
	{
		super();
	}

	public MalformedAlignmentException(String message)
	{
		super(message);
	}

	public MalformedAlignmentException(String message, Throwable cause)
	{
		super(message, cause);
	}

	public MalformedAlignmentException(Throwable cause)
	{
		super(cause);
	}
}
